package modelo.Entidades;

public class Entidad_Ciudades {

	
	private int idciudad;
	private String descripcion;
	private int Departamentos_idDepartamento;
	public int getIdciudad() {
		return idciudad;
	}
	public void setIdciudad(int idciudad) {
		this.idciudad = idciudad;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public int getDepartamentos_idDepartamento() {
		return Departamentos_idDepartamento;
	}
	public void setDepartamentos_idDepartamento(int departamentos_idDepartamento) {
		Departamentos_idDepartamento = departamentos_idDepartamento;
	}
	
	
	
}
