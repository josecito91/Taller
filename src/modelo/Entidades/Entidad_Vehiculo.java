package modelo.Entidades;

public class Entidad_Vehiculo {
	
	private int idvehiculo;
	private String chasis;
	private String placa;
	private int marca_idmarca;
	private int modelo_idmodelo;
	private int cliente_idcliente;
	public int getIdvehiculo() {
		return idvehiculo;
	}
	public void setIdvehiculo(int idvehiculo) {
		this.idvehiculo = idvehiculo;
	}
	public String getChasis() {
		return chasis;
	}
	public void setChasis(String chasis) {
		this.chasis = chasis;
	}
	public String getPlaca() {
		return placa;
	}
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	public int getMarca_idmarca() {
		return marca_idmarca;
	}
	public void setMarca_idmarca(int marca_idmarca) {
		this.marca_idmarca = marca_idmarca;
	}
	public int getModelo_idmodelo() {
		return modelo_idmodelo;
	}
	public void setModelo_idmodelo(int modelo_idmodelo) {
		this.modelo_idmodelo = modelo_idmodelo;
	}
	public int getCliente_idcliente() {
		return cliente_idcliente;
	}
	public void setCliente_idcliente(int cliente_idcliente) {
		this.cliente_idcliente = cliente_idcliente;
	}
	
	

}
